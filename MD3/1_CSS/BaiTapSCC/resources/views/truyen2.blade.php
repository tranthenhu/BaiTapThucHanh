                   因为不会用CC，所以用了主流的B站。以前午夜这个任务就很多人不喜欢做，更新之后更是变成了新人的噩梦，主要是两点。
1.喊救命的NPC只有10个左右了，以前是29个（大约）。寻找难度增加。
2.奸笑的声音由靠近才能听到变成随着杀手的远近而大小，这让新人玩家变的更为恐惧。
做这个视频主要是一个示范作用，如下几点记得就可以轻松做完午夜惊魂任务。
1.前30个奸笑是顺时针走动的，不会因为你而变动。找桶的同时听奸笑临近可以轻功到相反的方向去找。30个之后就有红光提示了，按照提示来找就轻松很多。
2.固定房间里的NPC和几个角落是不变的，三个小孩子还是有哭泣的声音，比较好找。
3.房间里全部找齐是7个。大房子4个，后面房子2个。右边房子里1个。
4.视频里很多没有提示而且找到的就是以前喊救命的固定NPC。
5.开望气的时候有雾气，雾气旁边会有声音提示，探索也会出桶，听到立刻停下探索，2次不成功就跑掉，但是骷髅的桶不要怼。
6.救30个之后就有红光提示了，这时候注意奸笑的走动速度变快，而且会根据你的位置寻找你。不要大意。
7.在送人的时候鬼会偷袭你，没有反应时间的，这时候可以用轻功反复在天上躲避。
8.如果有找不到的时候需要看攻略什么的，可以人物在水里，水里是安全的。
9.这个任务给两个称号，选择保持信任会得到“套路太深”，留着心眼会得到“机智如我”

